package clipper;

public class IntRect {
	public long left;
	public long top;
	public long right;
	public long bottom;

	public IntRect() {
	}

	public IntRect(long left, long top, long right, long bottom) {
		this.left = left;
		this.top = top;
		this.right = right;
		this.bottom = bottom;
	}
}
